export const generateEpisodes = (seasonNumber: number): Episode[] => {
  return Array.from({ length: 8 }, (_, i) => ({
    id: `s${seasonNumber}e${i + 1}`,
    title: `Episode ${i + 1}`,
    episodeNumber: i + 1,
    seasonNumber,
    duration: '42 min',
    year: 2024,
    rating: (8 + Math.random() * 2).toFixed(1),
    thumbnail: `https://picsum.photos/seed/s${seasonNumber}e${i + 1}/400/225`,
    description: `An intense episode from Season ${seasonNumber} that will keep you on the edge of your seat.`,
    genres: ['Action', 'Thriller'],
    progress: Math.random() > 0.7 ? Math.random() * 100 : undefined,
  }));
};

